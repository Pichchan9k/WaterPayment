package th.co.aware.bean;

public class History {
	
	private Integer h_id;
	private Integer h_unit;
	private Integer h_total;
	private String h_status;
	private Integer c_id;
	private String h_date;
	public Integer getH_id() {
		return h_id;
	}
	public void setH_id(Integer h_id) {
		this.h_id = h_id;
	}
	public Integer getH_unit() {
		return h_unit;
	}
	public void setH_unit(Integer h_unit) {
		this.h_unit = h_unit;
	}
	public Integer getH_total() {
		return h_total;
	}
	public void setH_total(Integer h_total) {
		this.h_total = h_total;
	}
	public String getH_status() {
		return h_status;
	}
	public void setH_status(String h_status) {
		this.h_status = h_status;
	}
	public Integer getC_id() {
		return c_id;
	}
	public void setC_id(Integer c_id) {
		this.c_id = c_id;
	}
	public String getH_date() {
		return h_date;
	}
	public void setH_date(String h_date) {
		this.h_date = h_date;
	}
	
	
	
}	
