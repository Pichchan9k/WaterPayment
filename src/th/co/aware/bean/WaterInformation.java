package th.co.aware.bean;

public class WaterInformation {
	private Integer w_id;
	private Integer w_branchno;
	private Integer w_areano;
	private Integer w_registrationno;
	private String w_address;
	private String w_name;
	
	
	public String getW_name() {
		return w_name;
	}
	public void setW_name(String w_name) {
		this.w_name = w_name;
	}
	public Integer getW_id() {
		return w_id;
	}
	public void setW_id(Integer w_id) {
		this.w_id = w_id;
	}
	public Integer getW_branchno() {
		return w_branchno;
	}
	public void setW_branchno(Integer w_branchNo) {
		this.w_branchno = w_branchNo;
	}
	public Integer getW_areano() {
		return w_areano;
	}
	public void setW_areano(Integer w_areaNo) {
		this.w_areano = w_areaNo;
	}
	public Integer getW_registrationno() {
		return w_registrationno;
	}
	public void setW_registrationno(Integer w_registrationNo) {
		this.w_registrationno = w_registrationNo;
	}
	public String getW_address() {
		return w_address;
	}
	public void setW_address(String w_address) {
		this.w_address = w_address;
	}
	
	
}
